'''Map Function?
    map() function returns a map object of the results after applying the given function to each
    item of a given iterable.
    one or more iterables can be passed using the map() function.
        Syntax :
            map(fun, iter)
Example:'''

'''numbers1 = [1, 2, 3]
numbers2 = [4, 5, 6]
result = map(lambda x, y: x + y, numbers1, numbers2)
print(list(result))'''

'''l = ['sat', 'bat', 'cat', 'mat']
test = list(map(list, l))
print(test)'''