'''Arguments?
        Arguments are the information that are passed in a function
        Arguments are enclosed in braces separated by comma.
        only two arguments can be passed in a function.'''

#Passing a List as an Argument?
#   A list can be passed as an argument with any data type.

def my_function(food):
  for x in food:
    print(x)

fruits = ["apple", "banana", "cherry"]

my_function(fruits)

